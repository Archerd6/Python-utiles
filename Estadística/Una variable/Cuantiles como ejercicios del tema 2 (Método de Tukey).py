def valor(n):
    if(n < 4):
        return ((1/4)*n)
    elif(n < 10):
        return ((1/10)*n)
    elif(n < 100):
        return ((1/100)*n)
    
    return(1)

def cuantil(c,vector):
    turkey=len(vector)*valor(c)
    if (turkey).is_integer():
        return((vector[int(turkey-1)]+vector[int(turkey)])/2)
    else:
        return(vector[int(turkey)])

